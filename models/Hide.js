const { Model, DataTypes } = require('sequelize');
const sequelize = require('../config/connection');

class Hide extends Model{}

Hide.init(
    {
        user_id: {
            type: DataTypes.INTEGER,
            references: {
                model: 'user',
                key: 'id'
            }
        },
        post_id: {
            type: DataTypes.INTEGER,
            references: {
                model: 'post',
                key: 'id'
            }
        }
    },
    {
        sequelize,
        timestamps: false,
        freezeTableName: true,
        underscored: true,
        modelName: 'hide'
    }
);

module.exports = Hide;